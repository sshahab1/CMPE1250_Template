/********************************************************************/
// Main Entry
/********************************************************************/
void main(void)
{
int i=0;
  // Initialize the processor, device, and peripherals
  // main entry point
  _DISABLE_COP();
  EnableInterrupts;

  /********************************************************************/
  // one-time initializations
  /********************************************************************/
  SWL_Init();
  RTI_Init();
  sci0_Init();
  Clock_Set20MHZ();
  Segs_Init();

  /********************************************************************/
  // main program loop
  /********************************************************************/
  for (;;)
  {
     Delay(100);
     
     Segs_Normal(4, '3', Segs_DP_OFF);

     if(i<9)
     {
        if(SWL_Pushed(SWL_UP))
        {
          Segs_16D(i, Segs_LineTop);
          i++;
        }
      
     }
    
    

  }
}
